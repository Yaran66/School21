#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t size)
{
	if (dest < src)
	{
		ft_memcpy(dest, src, size);
	}
	else if (dest > src)
	{
		while (size--)
		{
			((char *)dest)[size] = ((char *)src)[size];
		}
	}
	return (dest);
}
